Route::post('/sms', function (Request $request) {

    $message = strtolower($request->input('Body'));
    $from = $request->input('From');

    $calloffPhrases = [
        'call off',
        'call of',
        'called off',
        'calling off',
        'sick',
        'not coming',
        'not coming in',
        'cant make it',
        "can't make it",
        'cannot make it',
        'wont make it',
        "won't make it",
        'cant come in',
        "can't come in",
        'cannot come in',
        'not going in',
        'miss work',
        'missing work',
        'out today',
        'car broke down',
        'family emergency',
        'emergency',
        'no ride',
        'running a fever',
        'throwing up',
        'injured',
        'hospital',
        'no puedo ir',
        'no puedo llegar',
        'no voy a ir',
        'no ire',
        'no iré',
        'no puedo trabajar',
        'me siento enfermo',
        'me siento enferma',
        'estoy enfermo',
        'estoy enferma',
        'estoy malo',
        'estoy mala',
        'me enferme',
        'me enfermé',
        'tengo fiebre',
        'vomitando',
        'tengo emergencia',
        'emergencia familiar',
        'se me descompuso el carro',
        'no tengo transporte',
        'no tengo ride',
        'no puedo presentarme',
        'no puedo asistir',
    ];

    $status = 'OTHER';
    $reason = 'Other';	

foreach ($calloffPhrases as $phrase) {

    if (str_contains($message, $phrase)) {

        $status = 'CALLOFF';

        if (
            str_contains($message, 'sick') ||
            str_contains($message, 'fever') ||
            str_contains($message, 'hospital') ||
            str_contains($message, 'enfermo') ||
            str_contains($message, 'enferma')
        ) {

            $reason = 'Sick';

        } elseif (
            str_contains($message, 'car') ||
            str_contains($message, 'ride') ||
            str_contains($message, 'transport') ||
            str_contains($message, 'traffic')
        ) {

            $reason = 'Transportation';

        } elseif (
            str_contains($message, 'family') ||
            str_contains($message, 'emergency')
        ) {

            $reason = 'Family Emergency';

        } else {

            $reason = 'General Call-Off';

        }

        break;
    }
}

$recentDuplicate = DB::table('messages')
    ->where('from', $from)
    ->whereIn('status', ['CALLOFF', 'DUPLICATE'])
    ->where('body', $message)
    ->where('created_at', '>=', now()->subMinutes(10))
    ->exists();

$finalStatus = $status;

if ($status === 'CALLOFF' && $recentDuplicate) {
    $finalStatus = 'DUPLICATE';
}

    $messageId = DB::table('messages')->insertGetId([
        'from' => $from,
        'body' => $message,
        'status' => $finalStatus,
	'reason' => $reason,
        'created_at' => now(),
        'updated_at' => now(),
    ]);

    $employee = DB::table('employees')
        ->where('phone', $from)
        ->first();

    $name = $employee ? $employee->name : 'Unknown Employee';

    $clientName = $employee
        ? $employee->client_name
        : 'N/A';

    $client = DB::table('clients')
        ->where('name', strtolower($clientName))
        ->first();

    if ($finalStatus === 'CALLOFF') {

        $emailBody = "
CALL OFF ALERT

Employee: $name

Client: $clientName

Phone: $from

Reason: $reason

Time: " . now()->format('m/d/Y g:i A') . "

Message:
$message

This alert was automatically generated by CallOffApp.
";

        if ($client && $client->notify_email && $client->notification_email) {
try {

    Mail::raw($emailBody, function ($mail) use ($client, $name) {

        $mail->to($client->notification_email)
            ->subject("Call Off Alert - $name");

    });

    DB::table('messages')
        ->where('id', $messageId)
        ->update([
            'notification_sent' => true,
            'notification_sent_at' => now(),
        ]);

} catch (\Exception $e) {

    DB::table('messages')
        ->where('id', $messageId)
        ->update([
            'notification_error' => $e->getMessage(),
        ]);

}
        }
    }

    return response(
        '<Response><Message>Your call-off has been received.</Message></Response>',
        200
    )->header('Content-Type', 'text/xml');
});

