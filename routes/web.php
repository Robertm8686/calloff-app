        'today_calloffs' => DB::table('messages')
            ->where('status', 'CALLOFF')
            ->whereDate('created_at', today())
            ->count(),

        'week_calloffs' => DB::table('messages')
            ->where('status', 'CALLOFF')
            ->whereBetween('created_at', [
                now()->startOfWeek(),
                now()->endOfWeek()
            ])
            ->count(),

